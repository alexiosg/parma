.onAttach <- function(lib, pkg) {
	if(interactive() || getOption("verbose"))
		packageStartupMessage(sprintf("Package %s (%s) loaded.  To cite, see citation(\"%s\")\n",
						pkg, utils::packageDescription(pkg)$Version, pkg))
}